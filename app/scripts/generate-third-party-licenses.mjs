import { readFile, writeFile } from 'node:fs/promises';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

import licenseChecker from 'license-checker';

const scriptDir = path.dirname(fileURLToPath(import.meta.url));
const appDir = path.resolve(scriptDir, '..');
const licensesDir = path.join(appDir, 'licenses');
const outputPath = path.join(appDir, 'public', 'third-party-licenses.txt');
const repositoryLicensePath = path.resolve(appDir, '..', 'LICENSE');
const distributionLicensePath = path.join(appDir, 'public', 'LICENSE.txt');
const checkOnly = process.argv.includes('--check');
// package.json の licenses:check と同じ方針。vendored/WASM候補もnpm検査を迂回できない。
const ALLOWED_LICENSES = new Set([
  'MIT',
  'ISC',
  'Apache-2.0',
  'BSD-2-Clause',
  'BSD-3-Clause',
  'MPL-2.0',
  'CC-BY-3.0',
  'CC0-1.0',
  '0BSD',
  'Unlicense',
]);

function scanProductionDependencies() {
  return new Promise((resolve, reject) => {
    licenseChecker.init(
      {
        start: appDir,
        production: true,
        excludePrivatePackages: true,
      },
      (error, packages) => {
        if (error) reject(error);
        else resolve(packages);
      },
    );
  });
}

function normalizeText(value) {
  return value.replace(/\r\n/g, '\n').trim();
}

async function loadNpmNotices() {
  const packages = await scanProductionDependencies();

  return Promise.all(
    Object.entries(packages).map(async ([nameAndVersion, metadata]) => {
      if (!metadata.licenseFile) {
        throw new Error(`${nameAndVersion} にライセンス本文ファイルがありません。`);
      }

      return {
        nameAndVersion,
        license: String(metadata.licenses),
        repository: metadata.repository,
        text: normalizeText(await readFile(metadata.licenseFile, 'utf8')),
      };
    }),
  );
}

async function loadVendoredNotices() {
  const manifestPath = path.join(licensesDir, 'vendor-licenses.json');
  const manifest = JSON.parse(await readFile(manifestPath, 'utf8'));

  if (!Array.isArray(manifest)) {
    throw new Error('vendor-licenses.json は配列である必要があります。');
  }

  return Promise.all(
    manifest.map(async (entry, index) => {
      if (
        !entry ||
        typeof entry.name !== 'string' ||
        !entry.name ||
        typeof entry.version !== 'string' ||
        !entry.version ||
        typeof entry.license !== 'string' ||
        typeof entry.licenseFile !== 'string' ||
        !entry.licenseFile
      ) {
        throw new Error(`vendor-licenses.json[${index}] の必須項目が不正です。`);
      }
      if (!ALLOWED_LICENSES.has(entry.license)) {
        throw new Error(
          `${entry.name}@${entry.version} のライセンス ${entry.license} は許可リスト外です。`,
        );
      }
      const licensePath = path.resolve(licensesDir, entry.licenseFile);
      if (!licensePath.startsWith(`${licensesDir}${path.sep}`)) {
        throw new Error(`${entry.name}@${entry.version} のlicenseFileが範囲外です。`);
      }
      return {
        nameAndVersion: `${entry.name}@${entry.version}`,
        license: entry.license,
        repository: entry.repository,
        text: normalizeText(await readFile(licensePath, 'utf8')),
      };
    }),
  );
}

function renderNotice(notices) {
  const divider = '='.repeat(80);
  const sections = notices
    .sort((a, b) => a.nameAndVersion.localeCompare(b.nameAndVersion, 'en'))
    .map((notice) =>
      [
        divider,
        notice.nameAndVersion,
        `License: ${notice.license}`,
        notice.repository ? `Source: ${notice.repository}` : undefined,
        '-'.repeat(80),
        notice.text,
      ]
        .filter(Boolean)
        .join('\n'),
    );

  return [
    'THIRD-PARTY SOFTWARE NOTICES',
    '',
    'This file is generated by `npm run licenses:generate`.',
    'It covers production npm dependencies and source-derived vendored components',
    'included in the browser distribution. Do not edit it manually.',
    '',
    ...sections,
    '',
  ].join('\n');
}

const expected = renderNotice([
  ...(await loadNpmNotices()),
  ...(await loadVendoredNotices()),
]);
const expectedRepositoryLicense = await readFile(repositoryLicensePath, 'utf8');

if (checkOnly) {
  const actual = await readFile(outputPath, 'utf8').catch(() => '');
  if (normalizeText(actual) !== normalizeText(expected)) {
    throw new Error(
      'third-party-licenses.txt が依存関係と一致しません。' +
        '`npm run licenses:generate` を実行してください。',
    );
  }
  const distributedLicense = await readFile(distributionLicensePath, 'utf8').catch(
    () => '',
  );
  if (normalizeText(distributedLicense) !== normalizeText(expectedRepositoryLicense)) {
    throw new Error(
      'public/LICENSE.txt がリポジトリのLICENSEと一致しません。' +
        '`npm run licenses:generate` を実行してください。',
    );
  }
  console.log('Distribution license files are up to date.');
} else {
  await Promise.all([
    writeFile(outputPath, expected, 'utf8'),
    writeFile(distributionLicensePath, expectedRepositoryLicense, 'utf8'),
  ]);
  console.log(
    `Wrote ${path.relative(appDir, outputPath)} and ` +
      `${path.relative(appDir, distributionLicensePath)}.`,
  );
}
