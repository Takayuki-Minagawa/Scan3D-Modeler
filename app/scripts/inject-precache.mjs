import { createHash } from 'node:crypto';
import { readdir, readFile, writeFile } from 'node:fs/promises';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const scriptDir = path.dirname(fileURLToPath(import.meta.url));
const distDir = path.resolve(scriptDir, '..', 'dist');
const workerPath = path.join(distDir, 'coi-serviceworker.js');

async function listFiles(directory, prefix = '') {
  const entries = await readdir(directory, { withFileTypes: true });
  const files = [];
  for (const entry of entries) {
    const relative = path.posix.join(prefix, entry.name);
    if (entry.isDirectory()) files.push(...(await listFiles(path.join(directory, entry.name), relative)));
    else files.push(relative);
  }
  return files;
}

const files = (await listFiles(distDir))
  .filter((file) => file !== 'coi-serviceworker.js')
  .sort((a, b) => a.localeCompare(b, 'en'));
if (!files.includes('index.html')) throw new Error('dist/index.html がありません。Vite build後に実行してください。');

function startupFilesFromIndex(indexHtml) {
  const startupFiles = new Set(['index.html']);
  let moduleScriptFound = false;
  for (const match of indexHtml.matchAll(/<(script|link)\b[^>]*>/gi)) {
    const tagName = match[1].toLowerCase();
    const tag = match[0];
    const isStartupResource =
      (tagName === 'script' && /\btype=["']module["']/i.test(tag)) ||
      (tagName === 'link' && /\brel=["']stylesheet["']/i.test(tag));
    if (!isStartupResource) continue;

    const pathMatch = tag.match(/\b(?:src|href)=["']\.\/([^"']+)["']/i);
    if (!pathMatch) {
      throw new Error(`index.html の起動資源が同一スコープ相対パスではありません: ${tag}`);
    }
    if (!files.includes(pathMatch[1])) {
      throw new Error(`index.html が参照する起動資源がdistにありません: ${pathMatch[1]}`);
    }
    startupFiles.add(pathMatch[1]);
    if (tagName === 'script') moduleScriptFound = true;
  }
  if (!moduleScriptFound) throw new Error('index.html に起動用module scriptがありません。');
  return startupFiles;
}

const indexHtml = await readFile(path.join(distDir, 'index.html'), 'utf8');
const shellFiles = startupFilesFromIndex(indexHtml);
const shellPrecachePaths = [
  './',
  ...[...shellFiles].sort((a, b) => a.localeCompare(b, 'en')).map((file) => `./${file}`),
];
const optionalPrecachePaths = files
  .filter((file) => !shellFiles.has(file))
  .map((file) => `./${file}`);

const hash = createHash('sha256');
for (const file of files) {
  hash.update(file);
  hash.update(await readFile(path.join(distDir, file)));
}
const buildId = hash.digest('hex').slice(0, 16);

let worker = await readFile(workerPath, 'utf8');
const buildMarker = "/*__SCAN2FEM_BUILD_ID__*/ 'source'";
const shellPrecachePattern = /\/\*__SCAN2FEM_SHELL_PRECACHE_PATHS__\*\/ \[[\s\S]*?\];/;
const optionalPrecachePattern = /\/\*__SCAN2FEM_OPTIONAL_PRECACHE_PATHS__\*\/ \[[\s\S]*?\];/;
if (
  !worker.includes(buildMarker) ||
  !shellPrecachePattern.test(worker) ||
  !optionalPrecachePattern.test(worker)
) {
  throw new Error('coi-serviceworker.js のprecache注入マーカーが見つかりません。');
}
worker = worker.replace(buildMarker, `'${buildId}'`);
worker = worker.replace(
  shellPrecachePattern,
  `/* generated by scripts/inject-precache.mjs */ ${JSON.stringify(shellPrecachePaths, null, 2)};`,
);
worker = worker.replace(
  optionalPrecachePattern,
  `/* generated by scripts/inject-precache.mjs */ ${JSON.stringify(optionalPrecachePaths, null, 2)};`,
);
await writeFile(workerPath, worker, 'utf8');
console.log(
  `Injected ${shellPrecachePaths.length} shell and ${optionalPrecachePaths.length} optional ` +
    `offline resources (build ${buildId}).`,
);
